# import the math module for more complex math functions 
import math 

# calling the math math module, and using the ceil function which is the closest highest number
print(math.ceil(2.9))

# calling the math module, and using the floor function to return the closets lower number
print(math.floor(2.9))

# round funtion will round the number to the nearest integer 
x = 2.9
print(round(x))

# absolute function will always return a positive representatioin of that value 
y = -2.9
print(abs(y))

