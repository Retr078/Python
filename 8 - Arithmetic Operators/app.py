# this will give you a floating point
print(10 / 3)

# this will give you a integer
print(10 // 3)

# this is called a modules, it returns the remainder of the divition
print(10 % 3)

# this means to the power of 
print(10 ** 3)

# we can do math like this as well, by incrementing 
x = 10
x = x + 3
print(x)

# augmented assignment operator is a way to write the same code but in a shorter way, line 15 and line 20 are the same thing
x = 10
x += 3
print(x)

# we can also you the minus operator
x = 10 
x -= 3
print(x)


